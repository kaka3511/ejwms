package com.huaao.model.home;

public class AccountUser {
	
	private String userAccount;   //登录帐号
	private String userPassword;   //登录密码
	private String communityPassword;   //社区密码

	public String getUserAccount() {
		return userAccount;
	}
	
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	
	public String getUserPassword() {
		return userPassword;
	}
	
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public String getCommunityPassword() {
		return communityPassword;
	}

	public void setCommunityPassword(String communityPassword) {
		this.communityPassword = communityPassword;
	}
	
	
}
